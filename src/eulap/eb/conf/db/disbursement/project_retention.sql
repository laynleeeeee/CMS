
-- Description	: Create script for PROJECT_RETENTION.

DROP TABLE IF EXISTS PROJECT_RETENTION;

CREATE TABLE PROJECT_RETENTION (
	PROJECT_RETENTION_ID int(10) unsigned NOT NULL AUTO_INCREMENT,
	SEQUENCE_NO int(10) unsigned NOT NULL,
	FORM_WORKFLOW_ID int(10) unsigned NOT NULL,
	EB_OBJECT_ID int(10) unsigned DEFAULT NULL,
	COMPANY_ID int(10) unsigned NOT NULL,
	DIVISION_ID INT(10) UNSIGNED DEFAULT NULL,
	SALES_ORDER_ID int(10) unsigned NOT NULL,
	PROJECT_RETENTION_TYPE_ID int(10) unsigned NOT NULL,
	PO_NUMBER varchar(50) DEFAULT NULL,
	AR_CUSTOMER_ID int(10) unsigned NOT NULL,
	AR_CUSTOMER_ACCOUNT_ID int(10) unsigned NOT NULL,
	DATE date NOT NULL,
	DUE_DATE date NOT NULL,
	CURRENCY_ID INT(10) UNSIGNED DEFAULT NULL,
	CURRENCY_RATE_ID INT(10) UNSIGNED DEFAULT NULL,
	CURRENCY_RATE_VALUE double DEFAULT 0,
	WT_ACCOUNT_SETTING_ID int(10) unsigned DEFAULT NULL,
	WT_AMOUNT double DEFAULT '0',
	WT_VAT_AMOUNT double DEFAULT '0',
	AMOUNT double DEFAULT 0,
	REMARKS text DEFAULT NULL,
	DATE_RECEIVED date DEFAULT NULL,
	RECEIVER varchar(50) DEFAULT NULL,
	CREATED_BY INT(10) UNSIGNED NOT NULL,
	CREATED_DATE TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	UPDATED_BY INT(10) UNSIGNED NOT NULL,
	UPDATED_DATE TIMESTAMP NOT NULL DEFAULT '0000-00-00 00:00:00',
	PRIMARY KEY (PROJECT_RETENTION_ID),
	KEY FK_PR_FORM_WORKFLOW_ID (FORM_WORKFLOW_ID),
	KEY FK_PR_EB_OBJECT_ID (EB_OBJECT_ID),
	KEY FK_PR_COMPANY_ID (COMPANY_ID),
	KEY FK_PR_DIVISION_ID (DIVISION_ID),
	KEY FK_PR_SALES_ORDER_ID (SALES_ORDER_ID),
	KEY FK_PR_PROJECT_RETENTION_TYPE_ID (PROJECT_RETENTION_TYPE_ID),
	KEY FK_PR_AR_CUSTOMER_ID (AR_CUSTOMER_ID),
	KEY FK_PR_AR_CUSTOMER_ACCOUNT_ID (AR_CUSTOMER_ACCOUNT_ID),
	KEY FK_PR_CURRENCY_ID (CURRENCY_ID),
	KEY FK_PR_CURRENCY_RATE_ID (CURRENCY_RATE_ID),
	KEY FK_PR_WT_ACCOUNT_SETTING_ID (WT_ACCOUNT_SETTING_ID),
	KEY FK_PR_CREATED_BY (CREATED_BY),
	KEY FK_PR_UPDATED_BY (UPDATED_BY),
	CONSTRAINT FK_PR_FORM_WORKFLOW_ID FOREIGN KEY (FORM_WORKFLOW_ID) REFERENCES FORM_WORKFLOW (FORM_WORKFLOW_ID),
	CONSTRAINT FK_PR_EB_OBJECT_ID FOREIGN KEY (EB_OBJECT_ID) REFERENCES EB_OBJECT (EB_OBJECT_ID),
	CONSTRAINT FK_PR_COMPANY_ID FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY (COMPANY_ID),
	CONSTRAINT FK_PR_DIVISION_ID FOREIGN KEY (DIVISION_ID) REFERENCES DIVISION (DIVISION_ID),
	CONSTRAINT FK_PR_SALES_ORDER_ID FOREIGN KEY (SALES_ORDER_ID) REFERENCES SALES_ORDER (SALES_ORDER_ID),
	CONSTRAINT FK_PR_PROJECT_RETENTION_TYPE_ID FOREIGN KEY (PROJECT_RETENTION_TYPE_ID) REFERENCES PROJECT_RETENTION_TYPE (PROJECT_RETENTION_TYPE_ID),
	CONSTRAINT FK_PR_AR_CUSTOMER_ID FOREIGN KEY (AR_CUSTOMER_ID) REFERENCES AR_CUSTOMER (AR_CUSTOMER_ID),
	CONSTRAINT FK_PR_AR_CUSTOMER_ACCOUNT_ID FOREIGN KEY (AR_CUSTOMER_ACCOUNT_ID) REFERENCES AR_CUSTOMER_ACCOUNT (AR_CUSTOMER_ACCOUNT_ID),
	CONSTRAINT FK_PR_CURRENCY_ID FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY (CURRENCY_ID),
	CONSTRAINT FK_PR_CURRENCY_RATE_ID FOREIGN KEY (CURRENCY_RATE_ID) REFERENCES CURRENCY_RATE (CURRENCY_RATE_ID),
	CONSTRAINT FK_PR_WT_ACCOUNT_SETTING_ID FOREIGN KEY (WT_ACCOUNT_SETTING_ID) REFERENCES WT_ACCOUNT_SETTING (WT_ACCOUNT_SETTING_ID),
	CONSTRAINT FK_PR_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES USER (USER_ID),
	CONSTRAINT FK_PR_UPDATED_BY FOREIGN KEY (UPDATED_BY) REFERENCES USER (USER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;