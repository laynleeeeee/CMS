
-- Description: Insert script for fleet default account.

INSERT INTO FLEET_DEFAULT_ACCOUNT 
(ACCOUNT_ID, ACTIVE, CREATED_BY, CREATED_DATE, UPDATED_BY, UPDATED_DATE) SELECT ACCOUNT_ID, 1, 1, NOW(), 1, NOW() FROM ACCOUNT 
WHERE ACCOUNT_TYPE_ID = 1 OR ACCOUNT_TYPE_ID = 2 OR ACCOUNT_TYPE_ID = 7 OR ACCOUNT_NAME LIKE '%Sales%';
