
-- Description: sql script in creating LOAN_PROCEEDS table.

DROP TABLE IF EXISTS LOAN_PROCEEDS;

 CREATE TABLE LOAN_PROCEEDS (
  LOAN_PROCEEDS_ID int(10) unsigned NOT NULL AUTO_INCREMENT,
  FORM_WORKFLOW_ID int(10) unsigned DEFAULT NULL,
  EB_OBJECT_ID int(10) unsigned DEFAULT NULL,
  SEQUENCE_NO int(10) unsigned NOT NULL,
  COMPANY_ID int(10) unsigned NOT NULL,
  DIVISION_ID int(10) unsigned DEFAULT NULL,
  SUPPLIER_ID int(10) unsigned NOT NULL,
  SUPPLIER_ACCOUNT_ID int(10) unsigned NOT NULL,
  WT_ACCOUNT_SETTING_ID int(10) unsigned DEFAULT NULL,
  TERM_ID int(10) unsigned DEFAULT NULL,
  RECEIPT_METHOD_ID int(10) DEFAULT NULL,
  CURRENCY_ID INT(10) UNSIGNED DEFAULT NULL,
  CURRENCY_RATE_ID INT(10) UNSIGNED DEFAULT NULL,
  CURRENCY_RATE_VALUE double DEFAULT 0,
  WT_AMOUNT double DEFAULT '0.0',
  DATE date NOT NULL,
  GL_DATE date DEFAULT NULL,
  DESCRIPTION text,
  AMOUNT double NOT NULL,
  CREATED_BY int(10) unsigned NOT NULL,
  CREATED_DATE timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_BY int(10) unsigned NOT NULL,
  UPDATED_DATE timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  PRIMARY KEY (LOAN_PROCEEDS_ID),
  KEY FK_LOAN_PROCEEDS_COMPANY_ID (COMPANY_ID),
  KEY FK_LOAN_PROCEEDS_DIVISION_ID (DIVISION_ID),
  KEY FK_LOAN_PROCEEDS_WT_ACCOUNT_SETTING_ID (WT_ACCOUNT_SETTING_ID),
  KEY FK_LOAN_PROCEEDS_RECEIPT_METHOD_ID (RECEIPT_METHOD_ID),
  KEY FK_LOAN_PROCEEDS_SUPPLIER_ID (SUPPLIER_ID),
  KEY FK_LOAN_PROCEEDS_SUPPLIER_ACCOUNT_ID (SUPPLIER_ACCOUNT_ID),
  KEY FK_LOAN_PROCEEDS_TERM_ID (TERM_ID),
  KEY FK_LOAN_PROCEEDS_CREATED_BY (CREATED_BY),
  KEY FK_LOAN_PROCEEDS_UPDATED_BY (UPDATED_BY),
  KEY FK_LOAN_PROCEEDS_FORM_WORKFLOW_ID (FORM_WORKFLOW_ID),
  KEY FK_LOAN_PROCEEDS_CURRENCY_ID (CURRENCY_ID),
  KEY FK_LOAN_PROCEEDS_CURRENCY_RATE_ID (CURRENCY_RATE_ID),
  KEY FK_LOAN_PROCEEDS_EB_OBJECT_ID (EB_OBJECT_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_CURRENCY_ID FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY (CURRENCY_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_CURRENCY_RATE_ID FOREIGN KEY (CURRENCY_RATE_ID) REFERENCES CURRENCY_RATE (CURRENCY_RATE_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_WT_ACCOUNT_SETTING_ID FOREIGN KEY (WT_ACCOUNT_SETTING_ID) REFERENCES WT_ACCOUNT_SETTING (WT_ACCOUNT_SETTING_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_COMPANY_ID FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY (COMPANY_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_DIVISION_ID FOREIGN KEY (DIVISION_ID) REFERENCES DIVISION (DIVISION_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_RECEIPT_METHOD_ID FOREIGN KEY (RECEIPT_METHOD_ID) REFERENCES RECEIPT_METHOD (RECEIPT_METHOD_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_SUPPLIER_ID FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER (SUPPLIER_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_SUPPLIER_ACCOUNT_ID FOREIGN KEY (SUPPLIER_ACCOUNT_ID) REFERENCES SUPPLIER_ACCOUNT (SUPPLIER_ACCOUNT_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_TERM_ID FOREIGN KEY (TERM_ID) REFERENCES TERM (TERM_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES USER (USER_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_UPDATED_BY FOREIGN KEY (UPDATED_BY) REFERENCES USER (USER_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_FORM_WORKFLOW_ID FOREIGN KEY (FORM_WORKFLOW_ID) REFERENCES FORM_WORKFLOW (FORM_WORKFLOW_ID),
  CONSTRAINT FK_LOAN_PROCEEDS_EB_OBJECT_ID FOREIGN KEY (EB_OBJECT_ID) REFERENCES EB_OBJECT (EB_OBJECT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
